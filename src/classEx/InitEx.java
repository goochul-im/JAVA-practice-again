package classEx;

/**
 * Java 이니셜라이저 실행 순서 예제
 *
 * 이니셜라이저 5가지 종류와 실행 순서:
 * ────────────────────────────────────────────────────────────────
 * [클래스 로딩 시 1회만 실행]
 *   1. 클래스(static) 필드 이니셜라이저  - static 필드 선언 시 = 로 초기화
 *   2. 클래스(static) 이니셜라이저 블록 - static { ... } 블록
 *   (위 1, 2번은 코드 내 선언 순서대로 실행됨)
 *
 * [객체 생성 시마다 실행]
 *   3. 인스턴스 필드 이니셜라이저       - 인스턴스 필드 선언 시 = 로 초기화
 *   4. 인스턴스 이니셜라이저 블록       - { ... } 블록 (static 없음)
 *   5. 생성자                           - Constructor
 *   (위 3, 4번은 코드 내 선언 순서대로 실행됨)
 * ────────────────────────────────────────────────────────────────
 *
 * 핵심 포인트:
 * - static 이니셜라이저는 JVM이 클래스를 처음 메모리에 로드할 때 딱 1번만 실행된다.
 * - 인스턴스 이니셜라이저는 new 키워드로 객체를 생성할 때마다 반복 실행된다.
 * - 두 번째 객체를 생성하면 static 부분은 건너뛰고 인스턴스 + 생성자만 실행된다.
 */
public class InitEx {

    public static void main(String[] args) {

        System.out.println("======================================================");
        System.out.println("  Java 이니셜라이저 실행 순서 데모");
        System.out.println("======================================================");

        System.out.println("\n[ 첫 번째 객체 생성: new InitDemo() ]");
        System.out.println("------------------------------------------------------");
        // 클래스가 처음 로드되므로 static 이니셜라이저가 실행된 후
        // 인스턴스 이니셜라이저와 생성자가 이어서 실행된다.
        InitDemo obj1 = new InitDemo("obj1");

        System.out.println("\n[ 두 번째 객체 생성: new InitDemo() ]");
        System.out.println("------------------------------------------------------");
        // 클래스는 이미 로드된 상태이므로 static 이니셜라이저는 실행되지 않는다.
        // 인스턴스 이니셜라이저와 생성자만 실행된다.
        InitDemo obj2 = new InitDemo("obj2");

        System.out.println("\n======================================================");
        System.out.println("  실행 완료 - 콘솔 출력 순서를 확인하세요!");
        System.out.println("======================================================");
    }

}

/**
 * 이니셜라이저 종류별 실행 순서를 출력하는 데모 클래스
 */
class InitDemo {

    // ──────────────────────────────────────────────────────────────
    // [1] 클래스(static) 필드 이니셜라이저
    //     - 'static 필드명 = 값;' 형태로 선언과 동시에 초기화한다.
    //     - 클래스가 JVM에 로드되는 순간 딱 1번만 실행된다.
    //     - static 블록보다 먼저 선언되어 있으면 먼저 실행된다(선언 순서 기준).
    // ──────────────────────────────────────────────────────────────
    static String staticField1 = printAndReturn("[1] 클래스 필드 이니셜라이저  → staticField1 초기화");

    // ──────────────────────────────────────────────────────────────
    // [2] 클래스(static) 이니셜라이저 블록
    //     - 'static { ... }' 형태의 블록이다.
    //     - 클래스 로드 시 1번만 실행되며, 선언 순서대로 실행된다.
    //     - 복잡한 static 필드 초기화 로직(조건문, 반복문 등)에 활용한다.
    // ──────────────────────────────────────────────────────────────
    static {
        System.out.println("[2] 클래스 이니셜라이저 블록 (첫 번째 static 블록) 실행");
    }

    // ──────────────────────────────────────────────────────────────
    // [3] 인스턴스 필드 이니셜라이저
    //     - 'static' 없이 '필드명 = 값;' 형태로 선언과 동시에 초기화한다.
    //     - 객체가 생성될 때마다(new 호출 시) 매번 실행된다.
    //     - 인스턴스 블록보다 먼저 선언되어 있으면 먼저 실행된다(선언 순서 기준).
    // ──────────────────────────────────────────────────────────────
    String instanceField1 = printAndReturn("[3] 인스턴스 필드 이니셜라이저 → instanceField1 초기화");

    // ──────────────────────────────────────────────────────────────
    // [4] 인스턴스 이니셜라이저 블록
    //     - 'static' 없이 '{ ... }' 형태의 블록이다.
    //     - 객체 생성 시마다 생성자 본문 실행 전에 호출된다.
    //     - 모든 생성자에서 공통으로 수행할 초기화 로직을 여기에 작성한다.
    // ──────────────────────────────────────────────────────────────
    {
        System.out.println("[4] 인스턴스 이니셜라이저 블록 (첫 번째 인스턴스 블록) 실행");
    }

    // 두 번째 static 필드 이니셜라이저 - 선언 순서상 위의 static 블록 이후에 위치
    static String staticField2 = printAndReturn("[2-2] 클래스 필드 이니셜라이저  → staticField2 초기화 (두 번째 static 필드)");

    // 두 번째 static 이니셜라이저 블록 - 여러 개의 static 블록이 있으면 순서대로 모두 실행된다
    static {
        System.out.println("[2-3] 클래스 이니셜라이저 블록 (두 번째 static 블록) 실행");
    }

    // 두 번째 인스턴스 필드 이니셜라이저
    String instanceField2 = printAndReturn("[4-2] 인스턴스 필드 이니셜라이저 → instanceField2 초기화 (두 번째 인스턴스 필드)");

    // 두 번째 인스턴스 이니셜라이저 블록 - 여러 개의 인스턴스 블록도 순서대로 모두 실행된다
    {
        System.out.println("[4-3] 인스턴스 이니셜라이저 블록 (두 번째 인스턴스 블록) 실행");
    }

    // ──────────────────────────────────────────────────────────────
    // [5] 생성자 (Constructor)
    //     - 인스턴스 필드 이니셜라이저와 인스턴스 블록이 모두 끝난 후 마지막으로 실행된다.
    //     - 객체별로 다른 값을 받아 초기화하는 용도로 주로 사용한다.
    // ──────────────────────────────────────────────────────────────
    String name;

    InitDemo(String name) {
        this.name = name;
        System.out.println("[5] 생성자 실행 → name = \"" + name + "\" 으로 초기화 완료");
    }

    /**
     * 메시지를 출력하고 그 메시지를 반환하는 헬퍼 메서드.
     * 필드 이니셜라이저( = 표현식 ) 안에서 println을 호출하기 위해 사용한다.
     */
    static String printAndReturn(String message) {
        System.out.println(message);
        return message;
    }

}
